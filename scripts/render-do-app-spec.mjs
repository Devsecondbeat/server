#!/usr/bin/env node
/**
 * Renders .do/app.generated.yaml for staging from environment variables.
 * Loads .env.staging then .env (staging wins). Never logs secret values.
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import dotenv from 'dotenv';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');

const loadEnv = () => {
  const stagingPath = path.join(root, '.env.staging');
  const defaultPath = path.join(root, '.env');
  if (fs.existsSync(stagingPath)) {
    dotenv.config({ path: stagingPath });
  }
  if (fs.existsSync(defaultPath)) {
    dotenv.config({ path: defaultPath, override: false });
  }
};

const isPlaceholder = (value) =>
  !value ||
  value.includes('REPLACE') ||
  value.includes('your_') ||
  value.includes('your-');

// App Platform requires every env value to be a string (YAML must not emit bare booleans).
const yamlValue = (value) => JSON.stringify(String(value));

const plainEnv = (key, value) => `      - key: ${key}\n        value: ${yamlValue(value)}`;

const secretEnv = (key, value) =>
  `      - key: ${key}\n        scope: RUN_TIME\n        type: SECRET\n        value: ${yamlValue(value)}`;

const main = () => {
  loadEnv();

  const corsOrigin = process.env.CORS_ORIGIN?.replace(/\/$/, '');
  const anonKey = process.env.SUPABASE_ANON_KEY || process.env.SUPABASE_PUBLISHABLE_KEY;
  const serviceRoleKey = process.env.SUPABASE_SERVICE_ROLE_KEY;
  const activationRedirect =
    process.env.SUPABASE_ACTIVATION_REDIRECT_URL || `${corsOrigin}/activate`;
  const passwordResetRedirect =
    process.env.SUPABASE_PASSWORD_RESET_REDIRECT_URL || `${corsOrigin}/reset-password`;

  const required = {
    CORS_ORIGIN: corsOrigin,
    SUPABASE_URL: process.env.SUPABASE_URL,
    SUPABASE_ANON_KEY: anonKey,
    DATABASE_URL: process.env.DATABASE_URL,
    SENDGRID_API_KEY: process.env.SENDGRID_API_KEY,
    CLOUDFLARE_ACCOUNT_ID: process.env.CLOUDFLARE_ACCOUNT_ID,
    CLOUDFLARE_API_TOKEN: process.env.CLOUDFLARE_API_TOKEN,
    CLOUDFLARE_IMAGES_HASH: process.env.CLOUDFLARE_IMAGES_HASH,
  };

  const missing = Object.entries(required)
    .filter(([, value]) => isPlaceholder(value))
    .map(([key]) => key);

  if (corsOrigin?.includes('localhost')) {
    missing.push('CORS_ORIGIN (use staging client URL, not localhost)');
  }

  if (missing.length > 0) {
    console.error('Missing or placeholder environment variables:');
    missing.forEach((key) => console.error(`  - ${key}`));
    console.error('Copy .env.staging.example to .env.staging and fill values, or update .env.');
    process.exit(1);
  }

  if (isPlaceholder(serviceRoleKey)) {
    console.warn(
      'Warning: SUPABASE_SERVICE_ROLE_KEY not set — auth activation/reset emails will not work until set in DO dashboard.',
    );
  }

  const envBlock = [
    plainEnv('NODE_ENV', 'production'),
    plainEnv('PORT', '3000'),
    plainEnv('CORS_ORIGIN', corsOrigin),
    plainEnv('SUPABASE_URL', process.env.SUPABASE_URL),
    secretEnv('SUPABASE_ANON_KEY', anonKey),
    ...(isPlaceholder(serviceRoleKey) ? [] : [secretEnv('SUPABASE_SERVICE_ROLE_KEY', serviceRoleKey)]),
    secretEnv('DATABASE_URL', process.env.DATABASE_URL),
    plainEnv('SUPABASE_ACTIVATION_REDIRECT_URL', activationRedirect),
    plainEnv('SUPABASE_PASSWORD_RESET_REDIRECT_URL', passwordResetRedirect),
    secretEnv('SENDGRID_API_KEY', process.env.SENDGRID_API_KEY),
    plainEnv('SENDGRID_FROM_EMAIL', process.env.SENDGRID_FROM_EMAIL || 'support@secondbeat.in'),
    plainEnv('CLOUDFLARE_ACCOUNT_ID', process.env.CLOUDFLARE_ACCOUNT_ID),
    secretEnv('CLOUDFLARE_API_TOKEN', process.env.CLOUDFLARE_API_TOKEN),
    plainEnv('CLOUDFLARE_IMAGES_HASH', process.env.CLOUDFLARE_IMAGES_HASH),
    plainEnv('DB_FALLBACK_ENABLED', process.env.DB_FALLBACK_ENABLED || 'false'),
    plainEnv('DB_PREFERRED_SOURCE', process.env.DB_PREFERRED_SOURCE || 'supabase'),
    plainEnv('LOG_LEVEL', process.env.LOG_LEVEL || 'info'),
  ].join('\n');

  const spec = `# Generated by scripts/render-do-app-spec.mjs — do not commit
name: secondbeat-api-staging
region: blr

services:
  - name: api
    github:
      repo: Devsecondbeat/server
      branch: develop
      deploy_on_push: true
    environment_slug: node-js
    instance_count: 1
    instance_size_slug: basic-xxs
    run_command: npm run start:prod
    http_port: 3000
    health_check:
      http_path: /health
      initial_delay_seconds: 30
      period_seconds: 10
      timeout_seconds: 5
      success_threshold: 1
      failure_threshold: 3
    envs:
${envBlock}
`;

  const outPath = path.join(root, '.do/app.generated.yaml');
  fs.writeFileSync(outPath, spec);
  console.log(`Wrote ${path.relative(root, outPath)}`);
};

main();
